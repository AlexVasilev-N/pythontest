# По заданному номеру дня недели вывести его название


day = int (input ("Введите номер недели: "))


if day == 7:
    print ("Воскресенье")
if day == 6:
    print ("Суббота")
if day == 5:
    print ("Пятница")
if day == 4:
    print ("Четверг")
if day == 3:
    print ("Среда")
if day == 2:
    print ("Вторник")
if day == 1:
    print ("Понедельник")



